public class teamDetails_model {

    String name, code, shortName, crestURL;
    String selfURL, fixturesURL, playerURL;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getShortName() {
        return shortName;
    }

    public void setShortName(String shortName) {
        this.shortName = shortName;
    }

    public String getCrestURL() {
        return crestURL;
    }

    public void setCrestURL(String crestURL) {
        this.crestURL = crestURL;
    }

    public String getSelfURL() {
        return selfURL;
    }

    public void setSelfURL(String selfURL) {
        this.selfURL = selfURL;
    }

    public String getFixturesURL() {
        return fixturesURL;
    }

    public void setFixturesURL(String fixturesURL) {
        this.fixturesURL = fixturesURL;
    }

    public String getPlayerURL() {
        return playerURL;
    }

    public void setPlayerURL(String playerURL) {
        this.playerURL = playerURL;
    }
}
